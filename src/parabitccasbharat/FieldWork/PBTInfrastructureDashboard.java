    private void getAllLabels() {
        if(houselistingdata.getRoof()!=0){
            prommaterialroof.setText(PBTUtilities.getRoof(houselistingdata.getRoof()));
        }
        if(houselistingdata.getFloor()!=0){
            materialonfloor.setText(PBTUtilities.getFloor(houselistingdata.getFloor()));
        }
        if(houselistingdata.getWall()!=0){
            materialonwall.setText(PBTUtilities.getWall(houselistingdata.getWall()));
        }
        if(houselistingdata.getDwellingroom()!=null){
            dwellingwall.setText(houselistingdata.getDwellingroom());
        }
        if(houselistingdata.getFndyrofhouse()!=null){
            finyearcenhouse.setText(houselistingdata.getFndyrofhouse());
        }
        if(houselistingdata.getCndtofhouse()!=null){
            condnofhouse.setText(PBTUtilities.getCndtOfHouse(houselistingdata.getCndtofhouse()));
        }
        if(houselistingdata.getUseofhouse()!=null){
            useofhouse.setText(PBTUtilities.getUseofhouse(houselistingdata.getUseofhouse()));
        }
        if(houselistingdata.getPlotarea()!=null){
            plotareaofhouse.setText(houselistingdata.getPlotarea());
        }
        if(houselistingdata.getBuiltuparea()!=null){
            areaofchouse.setText(houselistingdata.getBuiltuparea());
        }
        if(houselistingdata.getNooftrees()!=null){
            nooftrees.setText(houselistingdata.getNooftrees());
        }
        if(houselistingdata.getNoofprottrees()!=null){
            noofplotttrees.setText(houselistingdata.getNoofprottrees());
        }
        if(houselistingdata.getAccessof_l()!=0){
            toilet.setText(PBTUtilities.getAccessoflatrine(houselistingdata.getAccessof_l()));
        }
        if(houselistingdata.getTypeof_l()!=0){
            typeoftoilet.setText(PBTUtilities.getTypesoflatrine(houselistingdata.getTypeof_l()));
        }
        if(houselistingdata.getDrainagesys()!=null){
            if(houselistingdata.getDrainagesys().equals("Y")){
                drainageYes.setSelected(true);
            }else{
                drainageNo.setSelected(true);
            }
        }
        if(houselistingdata.getBathfact()!=0){
            bathing.setText(PBTUtilities.getBathingfacility(houselistingdata.getBathfact()));
        }
        if(houselistingdata.getKitchen()!=0){
            typeofkitchen.setText(PBTUtilities.getKitchen(houselistingdata.getKitchen()));
        }
        if(houselistingdata.getCookfuel()!=0){
            typeofcookingfuel.setText(PBTUtilities.getCookfuel(houselistingdata.getCookfuel()));
        }

    }
