    private void updateAllFields() {
        regisdate.setMaxSelectableDate(new Date(System.currentTimeMillis()));
        if(industryData.getNameofbusiness()!=null){
            nameofbuisness.setText(industryData.getNameofbusiness());
        }
        if(industryData.getOfficetype()!=null){
            officetype.setText("" + industryData.getOfficetype());
        }
        if(industryData.getPrsntceoofcomp()!=null){
            prsntceoofcomp.setText(industryData.getPrsntceoofcomp());
        }
        if(industryData.getNooffememp()!=null){
            noofmaleemp.setText("" + industryData.getNooffememp());
        }
        if(industryData.getNoofmaleemp()!=null){
            nooffemaleemp.setText("" +industryData.getNoofmaleemp());
        }
        if(industryData.getNoofotheremp()!=null){
            noofotheremp.setText("" + industryData.getNoofotheremp());
        }
        if(industryData.getNoofshift()!=null){
            noofshift.setText("" + industryData.getNoofshift());
        }
        if(industryData.getExportprod()!=null){
            if (industryData.getExportprod().equals("Y")){
                exportyes.setSelected(true);
            }else{
                exportyes.setSelected(true);
            }
        }
        if(industryData.getCompregdate()!=null){
            regisdate.setDate(industryData.getCompregdate());
        }
    }

    private void addTextWathcers() {
        textWatchers.addNameTextWatcher(nameofbuisness, updateMap, "NameOfBusiness");
        regisdate.getDateEditor().addPropertyChangeListener(new PropertyChangeListener() {
            @Override
            public void propertyChange(PropertyChangeEvent evt) {
                if("date".equals(evt.getPropertyName())){
                   Date date = regisdate.getDate();
                    System.out.println(date.toString());
                    try {
                        updateMap.put("CompRegDate", new SimpleDateFormat("yyyy-MM-dd").format(new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy",Locale.US).parse(date.toString())));
                    } catch (ParseException ex) {
                        Logger.getLogger(PBTCompDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
        textWatchers.addNumberTextWatcher(officetype, updateMap, "OfficeType");
        textWatchers.addNameTextWatcher(prsntceoofcomp, updateMap, "PrsntCEOofComp");
        textWatchers.addNumberTextWatcher(noofmaleemp, updateMap, "NoOfMaleEmp");
        textWatchers.addNumberTextWatcher(nooffemaleemp, updateMap, "NoOfFemEMp");
        textWatchers.addNumberTextWatcher(noofotheremp, updateMap, "NoOfOtherEmp");
        textWatchers.addNumberTextWatcher(noofshift, updateMap, "NoOfShift");
        textWatchers.addYesNolistener(exportyes, exportno, updateMap, "ExportProd");
    }
