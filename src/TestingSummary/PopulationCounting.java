    private ComboBoxModel<String> getModel(String coloumn) {
        String query = getQuery(coloumn);
        return getModelWithData(query,coloumn);
    }

    private ComboBoxModel<String> getModelWithData(String query, String coloumn) {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement(coloumn);
        try {
            ParabitDBC db = new ParabitDBC();
            db.rs1 = db.stm.executeQuery(query);
            while (db.rs1.next()){
                String value = db.rs1.getString(1);
                model.addElement(value);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return model;
    }

    private String getQuery(String coloumn) {
        switch (coloumn){
            case STATE: 
                return Queries.getStateQuery();
            case DISTRICT:
                return Queries.getDistOfStateQuery(State.getSelectedItem().toString());
            case CITY:
                return Queries.getCityOfDistrictQuery(dist.getSelectedItem().toString());
            case BLOODGROUP:
                return Queries.getBloodGroupQuery();
            case MARTIALSTATUS:
                return Queries.getMartialStatusQuery();
            default:
                return "";
        }
    }

    private void changePopulation(boolean isReset) {
        String query = "SELECT COUNT(*) as pop FROM `pbtcensus_household`";
        if(!isReset) query += popQuery.getQuery();
        ParabitDBC db = new ParabitDBC();
        try {
            System.out.println(query);
            db.rs1 = db.stm.executeQuery(query);
            if(db.rs1.next()){
                Long pop = db.rs1.getLong(1);
                numberPad.addAnimaton(pop);
            }
        } catch (SQLException | InterruptedException ex) {
            ex.printStackTrace();
        }
    }
